// Creando un if

const puntaje = 1000;

// Dentro del () siempre va la condición, comparando con "=="
if(puntaje == 1000) {
    console.log("Si es igual...");
    //Este es el bloque de codigo  que se va a ejecutar cumplida la condición.
} else {
    //Si no se cumple se ejecuta la otra parte del codigo.
    console.log("No es igual...")
}