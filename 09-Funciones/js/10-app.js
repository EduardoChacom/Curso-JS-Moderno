const aprendiendo = function(){
    console.log("Aprendiendo HTML");
}

//El function se transforma en "=>" del lado derecho del ()
//No necesita llaves {} para funcionar.
//Dan por implicito el return, entonces no es necesario ponerlo.

const aprendiendo2 = () => "Aprendiendo JavaScript";


aprendiendo();
console.log(aprendiendo2());

